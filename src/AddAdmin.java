        /**
     * Create admin account with password hashing
     */
    private boolean createAdminAccount(String name, String email, String password) {
                try {
            // For now, store password as plain text to fit the 50-character database limit
            // In production, you should increase the password column size to at least 255 characters
            // and then use proper hashing
            
            if (password.length() > 50) {
                throw new RuntimeException("Password is too long for database field (max 50 characters)");
            }
            
            // Use the correct column name from your database schema (mail, not email)
            String sql = "INSERT INTO admin (name, mail, password) VALUES (?, ?, ?)";
            
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, name);
                pstmt.setString(2, email.toLowerCase()); // Store email in lowercase
                pstmt.setString(3, password); // Store plain text password for now
                
                int rowsAffected = pstmt.executeUpdate();
                return rowsAffected > 0;
            }
            
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Database error creating admin", e);
            throw new RuntimeException("Database error: " + e.getMessage());
        }
    }
    
    /**
     * Check if email already exists in database
     */
    private boolean emailExists(String mail) {
        try {
            String sql = "SELECT COUNT(*) FROM admin WHERE LOWER(mail) = LOWER(?)";
            
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, mail);
                
                try (ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        return rs.getInt(1) > 0;
                    }
                }
            }
            
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Database error checking email", e);
        }
        
        return false;
    }
    
    /**
     * Generate random salt for password hashing
     */
    private String generateSalt() {
        SecureRandom random = new SecureRandom();
        byte[] salt = new byte[16];
        random.nextBytes(salt);
        return Base64.getEncoder().encodeToString(salt);
    }
    
    /**
     * Hash password with salt using SHA-256
     */
    private String hashPassword(String password, String salt) throws NoSuchAlgorithmException {
        MessageDigest md = MessageDigest.getInstance("SHA-256");
        md.update(salt.getBytes());
        byte[] hashedPassword = md.digest(password.getBytes());
        return Base64.getEncoder().encodeToString(hashedPassword);
    }
    
    /**
     * Validate name format
     */
    private boolean isValidName(String name) {
        if (name == null || name.trim().length() < 2) {
            return false;
        }
        
        // Only letters, spaces, apostrophes, and hyphens allowed
        String namePattern = "^[a-zA-Z\\s'\\-]+$";
        return Pattern.matches(namePattern, name.trim());
    }
    
    /**
     * Validate email format
     */
    private boolean isValidEmail(String mail) {
        if (mail == null || mail.trim().isEmpty()) {
            return false;
        }
        
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        return Pattern.matches(emailRegex, mail);
    }
    
    /**
     * Validate password strength
     * Returns null if valid, error message if invalid
     */
    private String validatePassword(String password) {
        if (password == null || password.length() < 8) {
            return "Password must be at least 8 characters long!";
        }
        
        boolean hasUppercase = password.chars().anyMatch(Character::isUpperCase);
        boolean hasLowercase = password.chars().anyMatch(Character::isLowerCase);
        boolean hasDigit = password.chars().anyMatch(Character::isDigit);
        boolean hasSpecialChar = password.matches(".*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?].*");
        
        if (!hasUppercase) {
            return "Password must contain at least one uppercase letter!";
        }
        
        if (!hasLowercase) {
            return "Password must contain at least one lowercase letter!";
        }
        
        if (!hasDigit) {
            return "Password must contain at least one number!";
        }
        
        if (!hasSpecialChar) {
            return "Password must contain at least one special character (!@#$%^&*()_+-=[]{}|;':\"\\,.<>/?";
        }
        
        // Check for common weak passwords
        String[] weakPasswords = {
            "password", "12345678", "password123", "admin123", 
            "qwerty123", "abc12345", "password1", "123456789"
        };
        
        for (String weak : weakPasswords) {
            if (password.toLowerCase().contains(weak)) {
                return "Password is too common. Please choose a more secure password!";
            }
        }
        
        return null; // Password is valid
    }
    
