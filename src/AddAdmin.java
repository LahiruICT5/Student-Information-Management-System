    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                String name = jTextField2.getText().trim();
        String mail = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword());
        
        // Input validation
        if (name.isEmpty() || mail.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Please fill in all fields!", 
                "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Validate name (only letters and spaces, minimum 2 characters)
        if (!isValidName(name)) {
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid name (letters and spaces only, minimum 2 characters)!", 
                "Invalid Name", JOptionPane.WARNING_MESSAGE);
            jTextField2.requestFocus();
            return;
        }
        
        // Validate email format
        if (!isValidEmail(mail)) {
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid email address!", 
                "Invalid Email", JOptionPane.WARNING_MESSAGE);
            jTextField1.requestFocus();
            return;
        }
        
        // Validate password strength
        String passwordError = validatePassword(password);
        if (passwordError != null) {
            JOptionPane.showMessageDialog(this, 
                passwordError, 
                "Invalid Password", JOptionPane.WARNING_MESSAGE);
            jPasswordField1.requestFocus();
            return;
        }
        
        // Check if email already exists
        if (emailExists(mail)) {
            JOptionPane.showMessageDialog(this, 
                "An admin with this email already exists!", 
                "Email Already Exists", JOptionPane.WARNING_MESSAGE);
            jTextField1.requestFocus();
            return;
        }
        
        // Confirm admin creation
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Create new admin account for:\n" +
            "Name: " + name + "\n" +
            "Email: " + mail + "\n\n" +
            "Are you sure you want to proceed?", 
            "Confirm Admin Creation", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.QUESTION_MESSAGE);
        
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        // Create admin account
        try {
            if (createAdminAccount(name, mail, password)) {
                JOptionPane.showMessageDialog(this, 
                    "Admin account created successfully!\n" +
                    "Name: " + name + "\n" +
                    "Email: " + mail, 
                    "Success", JOptionPane.INFORMATION_MESSAGE);
                
                // Clear form after successful creation
                clearFields();
                
                // Ask if user wants to create another admin
                int createAnother = JOptionPane.showConfirmDialog(this, 
                    "Would you like to create another admin account?", 
                    "Create Another?", 
                    JOptionPane.YES_NO_OPTION);
                
                if (createAnother != JOptionPane.YES_OPTION) {
                    // Go back to home
                    setVisible(false);
                    Home object = new Home();
                    object.setVisible(true);
                }
                
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Failed to create admin account. Please try again.", 
                    "Creation Failed", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Error creating admin account", e);
            JOptionPane.showMessageDialog(this, 
                "An error occurred while creating the admin account:\n" + e.getMessage(), 
                "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

